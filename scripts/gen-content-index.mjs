// Generates lib/articleIndex.generated.ts — a tiny, edge-safe map of
// slug -> {title, category} so the per-article OG image (Edge runtime, no fs)
// can label itself. Runs on `prebuild` / `predev`. Safe to delete; regenerated.
import fs from "node:fs";
import path from "node:path";
import matter from "gray-matter";

const root = process.cwd();
const dir = path.join(root, "content", "articles");
const outFile = path.join(root, "lib", "articleIndex.generated.ts");

const files = fs.existsSync(dir)
  ? fs.readdirSync(dir).filter((f) => f.endsWith(".mdx"))
  : [];

const entries = files.map((f) => {
  const { data } = matter(fs.readFileSync(path.join(dir, f), "utf8"));
  return {
    slug: data.slug || f.replace(/\.mdx$/, ""),
    title: data.title || "",
    category: data.category || "",
  };
});

const body = `// AUTO-GENERATED by scripts/gen-content-index.mjs — do not edit by hand.
export type ArticleIndexEntry = { slug: string; title: string; category: string };
export const articleIndex: ArticleIndexEntry[] = ${JSON.stringify(entries, null, 2)};
`;

fs.mkdirSync(path.dirname(outFile), { recursive: true });
fs.writeFileSync(outFile, body);
console.log(`gen-content-index: wrote ${entries.length} entries`);
